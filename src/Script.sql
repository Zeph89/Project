CREATE TABLE COMPANY(
	ID int NOT NULL AUTO_INCREMENT,
	NAME VARCHAR(50),
	PRIMARY KEY (ID)
);

CREATE TABLE COMPUTER(
	ID int NOT NULL AUTO_INCREMENT,
	NAME VARCHAR(50),
	INTRODUCED_DATE TIMESTAMP(8),
	DISCONTINUED_DATE TIMESTAMP(8),
	COMPANY_ID int,
	PRIMARY KEY (ID),
	FOREIGN KEY (COMPANY_ID) REFERENCES COMPANY(ID)
);

